#include "libft.h"

void	ft_putstr(char const *s)
{
	int	i;

	i = 0;
	if (s == NULL)
		write(1, "(null)", 6);
	if (s)
	{
		while (s[i])
			write(1, &s[i++], 1);
	}
}
